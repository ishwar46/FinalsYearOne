package Polymorphism;

public class Arithmetic {
    void add (int a, int b){
        System.out.println(a+b);
    }
    void subtract (int a, int b){
        System.out.println(a - b);
    }
    void multiply (int a, int b){
        System.out.println(a * b);
    }
    void divide (int a, int b){
        System.out.println(a/b);
    }
}